public class BasicArithmetic {
    public static void main(String[] args) {
        double a = 10.5;
        double b = 5.2;

        System.out.println("Addition: " + (a + b));
        System.out.println("Subtraction: " + (a - b));
        System.out.println("Multiplication: " + (a * b));
        System.out.println("Division: " + (a / b));
    }
}
